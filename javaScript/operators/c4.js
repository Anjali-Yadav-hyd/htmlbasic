// bitwise

console.log(true & true)
// 1
console.log(true & false)
// 0
console.log(false & true)
// 0
console.log(false & false)
// 0

console.log(true | true)
// 1
console.log(true | false)
// 1
console.log(false | true)
// 1
console.log(false | false)
// 0
console.log(true * 6)
// 6
console.log(false * 7)
// 0

a = 10
b = 20
console.log(a & b)
// 0
console.log(a | b) 
// 30
console.log(a ^ b) 
// 30

a = 10
b = 2
console.log(a >> b)
// 2
console.log(a << b)
// 40