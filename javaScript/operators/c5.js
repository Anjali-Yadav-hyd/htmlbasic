// logical operators

console.log(true && true)
// true
console.log(true && false)
// false
console.log(false && true)
// false
console.log(false && false)
// false

console.log(true || true)
// true
console.log(true || false)
// true
console.log(false || true)
// true
console.log(false || false)
// false

a = 10
b = 20
console.log(a && b)
// 20
console.log(a || b) 
// 10