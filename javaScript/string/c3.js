// charAt()
userName = "Nameone"
console.log(userName.charAt(0))
// N
console.log(userName.charAt(2))
// m
console.log(userName.charAt(10))
// blank


// charCodeAt()
product = "Apple"
console.log(product.charCodeAt('0'))
// 65
console.log(product.charCodeAt('4'))
// 101

// includes
userName = "Nameone"
console.log(userName.includes('N'))
// true
console.log(userName.includes('f'))
// false


// indexOf()
userName = "K SAI KIRAN"
console.log(userName.indexOf('K'))
// 0
console.log(userName.indexOf('K', 3))
// 6

// lastindexOf()
console.log(userName.lastIndexOf('K'))
// 6
console.log(userName.indexOf('K', 3))
// 6

